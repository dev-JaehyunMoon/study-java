package study;

public class Customer {
	String name;
	String phoneNumber;
	int discount;
	int money;
	
	public Customer() {
	}

	public Customer(String name, String phoneNumber, int discount, int money) {
		super();
		this.name = name;
		this.phoneNumber = phoneNumber;
		this.discount = discount;
		this.money = money;
	}
	

}
